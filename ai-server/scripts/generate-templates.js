/**
 * generate-templates.js
 * 
 * Generates Carbone-compatible DOCX templates for TopRealty AI.
 * Run: node scripts/generate-templates.js
 * 
 * Carbone v3 tag syntax embedded as plain text:
 *   {d.field_name}          — simple field (d = data root)
 *   {#d.array}{.prop}{/d.array}  — array loop
 */

import {
  Document, Packer, Paragraph, TextRun, Table, TableRow, TableCell,
  Header, Footer, AlignmentType, HeadingLevel,
  BorderStyle, WidthType, ShadingType, convertInchesToTwip,
  PageOrientation, PageBreak,
} from 'docx';
import fs from 'node:fs';
import path from 'node:path';

const TEMPLATES_DIR = path.resolve(process.cwd(), 'templates');
const NAVY = '1A4175';
const MAROON = '941D28';
const WHITE = 'FFFFFF';
const LIGHT_GRAY = 'F5F5F5';
const DARK_TEXT = '1A1A1A';

// Ensure output dir exists
fs.mkdirSync(TEMPLATES_DIR, { recursive: true });

// ─── Helper Functions ───────────────────────────────────────────────

function tag(text) {
  return new TextRun({ text, font: 'Calibri', size: 22, color: DARK_TEXT });
}

function tagBold(text) {
  return new TextRun({ text, font: 'Calibri', size: 22, color: DARK_TEXT, bold: true });
}

function heading(text) {
  return new Paragraph({
    text,
    heading: HeadingLevel.HEADING_1,
    spacing: { before: 240, after: 120 },
  });
}

function subheading(text) {
  return new Paragraph({
    children: [new TextRun({ text, font: 'Calibri', size: 28, color: NAVY, bold: true })],
    spacing: { before: 200, after: 100 },
  });
}

function bodyLine(text) {
  return new Paragraph({
    children: [tag(text)],
    spacing: { after: 80 },
  });
}

function emptyLine() {
  return new Paragraph({ spacing: { after: 80 }, children: [new TextRun({ text: '' })] });
}

function divider() {
  return new Paragraph({
    children: [],
    border: { bottom: { color: NAVY, size: 1, space: 1, style: BorderStyle.SINGLE } },
    spacing: { before: 120, after: 120 },
  });
}

// Navy header section (full-width bar)
function headerBar(barText) {
  return new Paragraph({
    children: [new TextRun({ text: barText, font: 'Calibri', size: 28, color: WHITE, bold: true })],
    shading: { type: ShadingType.CLEAR, color: NAVY, fill: NAVY },
    spacing: { before: 0, after: 200 },
    indent: { left: convertInchesToTwip(0.5), right: convertInchesToTwip(0.5) },
  });
}

function footerLine() {
  return new Paragraph({
    children: [
      new TextRun({ text: 'Generated by ', font: 'Calibri', size: 18, color: '888888' }),
      new TextRun({ text: '{d.generated_date}', font: 'Calibri', size: 18, color: '888888', italics: true }),
      new TextRun({ text: '  |  TopRealty AI', font: 'Calibri', size: 18, color: NAVY }),
    ],
    alignment: AlignmentType.CENTER,
  });
}

// ─── 1. BROCHURE TEMPLATE ───────────────────────────────────────────

function buildBrochure() {
  return new Document({
    styles: {
      default: {
        document: {
          run: { font: 'Calibri', size: 22 },
        },
      },
    },
    sections: [{
      properties: { page: { margin: { top: 720, bottom: 720, left: 720, right: 720 } } },
      headers: {
        default: new Header({
          children: [
            new Paragraph({
              children: [
                new TextRun({ text: 'TOPREALTY', font: 'Calibri', size: 24, color: WHITE, bold: true }),
                new TextRun({ text: '  |  Philippine Real Estate', font: 'Calibri', size: 18, color: 'CCCCCC' }),
              ],
              shading: { type: ShadingType.CLEAR, color: NAVY, fill: NAVY },
              alignment: AlignmentType.RIGHT,
            }),
          ],
        }),
      },
      footers: {
        default: new Footer({ children: [footerLine()] }),
      },
      children: [
        // Title block
        new Paragraph({
          children: [
            new TextRun({ text: '{d.property_name}', font: 'Calibri', size: 44, color: NAVY, bold: true }),
          ],
          spacing: { after: 60 },
        }),
        new Paragraph({
          children: [
            new TextRun({ text: 'Property Brochure  |  Tone: ', font: 'Calibri', size: 20, color: '666666' }),
            new TextRun({ text: '{d.tone}', font: 'Calibri', size: 20, color: MAROON, bold: true }),
          ],
          spacing: { after: 30 },
        }),
        divider(),

        // Property details section (Carbone  converts \n to DOCX line breaks)
        subheading('Property Details'),
        bodyLine('{d.details}'),
        divider(),

        // Call to action
        new Paragraph({
          children: [
            new TextRun({ text: 'For inquiries, contact your TopRealty agent or visit www.toprealty.ai', font: 'Calibri', size: 20, color: NAVY, italics: true }),
          ],
          alignment: AlignmentType.CENTER,
          spacing: { before: 200 },
        }),
      ],
    }],
  });
}

// ─── 2. CMA REPORT TEMPLATE ─────────────────────────────────────────

function buildCma() {
  return new Document({
    styles: {
      default: { document: { run: { font: 'Calibri', size: 22 } } },
    },
    sections: [{
      properties: { page: { margin: { top: 720, bottom: 720, left: 720, right: 720 } } },
      headers: {
        default: new Header({
          children: [
            new Paragraph({
              children: [
                new TextRun({ text: 'COMPARATIVE MARKET ANALYSIS', font: 'Calibri', size: 24, color: WHITE, bold: true }),
              ],
              shading: { type: ShadingType.CLEAR, color: NAVY, fill: NAVY },
              alignment: AlignmentType.CENTER,
            }),
          ],
        }),
      },
      footers: {
        default: new Footer({ children: [footerLine()] }),
      },
      children: [
        // Title
        new Paragraph({
          children: [
            new TextRun({ text: 'CMA Report', font: 'Calibri', size: 40, color: NAVY, bold: true }),
          ],
          spacing: { after: 120 },
        }),
        divider(),

        // Subject property
        subheading('Subject Property'),
        bodyLine('{d.subject_name}'),
        bodyLine('Asking Price: {d.subject_price}'),
        divider(),

        // Comparable properties
        subheading('Comparable Properties'),
        bodyLine('{d.comparables}'),
        divider(),

        // Market analysis
        subheading('Market Trends & Analysis'),
        bodyLine('{d.market_trends}'),
        divider(),

        // Disclaimer
        new Paragraph({
          children: [
            new TextRun({ text: 'This CMA is prepared for informational purposes. All data sourced from public listings and market reports. Consult a licensed appraiser for formal valuation.', font: 'Calibri', size: 18, color: '888888', italics: true }),
          ],
          spacing: { before: 200 },
        }),
      ],
    }],
  });
}

// ─── 3. COMPARISON TEMPLATE ─────────────────────────────────────────

function buildComparison() {
  // Build comparison table dynamically (Carbone will loop over array)
  // Carbone syntax: {#d.properties}...{/d.properties}
  // Inside the loop: {.name}, {.price}, {.specs}, {.pros}, {.cons}
  
  const headerRow = new TableRow({
    tableHeader: true,
    children: [
      { text: 'Property', shading: NAVY, color: WHITE },
      { text: 'Price', shading: NAVY, color: WHITE },
      { text: 'Key Specs', shading: NAVY, color: WHITE },
      { text: 'Pros', shading: NAVY, color: WHITE },
      { text: 'Cons', shading: NAVY, color: WHITE },
    ].map(({ text: t, shading: s, color: c }) =>
      new TableCell({
        children: [new Paragraph({
          children: [new TextRun({ text: t, font: 'Calibri', size: 20, color: c, bold: true })],
          alignment: AlignmentType.CENTER,
        })],
        shading: { type: ShadingType.CLEAR, color: s, fill: s },
        width: { size: 20, type: WidthType.PERCENTAGE },
      })
    ),
  });

  // Data row template with Carbone loop
  const dataRow = new TableRow({
    children: [
      { tag: '{.name}', bold: true },
      { tag: '{.price}' },
      { tag: '{.specs}' },
      { tag: '{.pros}' },
      { tag: '{.cons}' },
    ].map(({ tag: t, bold: b }, i) =>
      new TableCell({
        children: [new Paragraph({
          children: [new TextRun({ text: t, font: 'Calibri', size: 20, color: DARK_TEXT, bold: b })],
        })],
        width: { size: 20, type: WidthType.PERCENTAGE },
        shading: { type: ShadingType.CLEAR, color: i % 2 === 0 ? WHITE : LIGHT_GRAY, fill: i % 2 === 0 ? WHITE : LIGHT_GRAY },
      })
    ),
  });

  return new Document({
    styles: {
      default: { document: { run: { font: 'Calibri', size: 22 } } },
    },
    sections: [{
      properties: { page: { margin: { top: 720, bottom: 720, left: 360, right: 360 } } },
      headers: {
        default: new Header({
          children: [
            new Paragraph({
              children: [
                new TextRun({ text: 'PROPERTY COMPARISON REPORT', font: 'Calibri', size: 24, color: WHITE, bold: true }),
              ],
              shading: { type: ShadingType.CLEAR, color: NAVY, fill: NAVY },
              alignment: AlignmentType.CENTER,
            }),
          ],
        }),
      },
      footers: {
        default: new Footer({ children: [footerLine()] }),
      },
      children: [
        // Title
        new Paragraph({
          children: [
            new TextRun({ text: 'Property Comparison', font: 'Calibri', size: 40, color: NAVY, bold: true }),
          ],
          spacing: { after: 200 },
        }),

        // Carbone data block — uses a single {d.details} field
        // (same pattern as brochure — AI formats the comparison as text)
        bodyLine('{d.details}'),
        divider(),

        // Disclaimer
        new Paragraph({
          children: [
            new TextRun({ text: 'Prices and availability subject to change. Verify with a licensed agent.', font: 'Calibri', size: 18, color: '888888', italics: true }),
          ],
          spacing: { before: 200 },
        }),
      ],
    }],
  });
}

// ─── Generate All ───────────────────────────────────────────────────

async function main() {
  const templates = [
    { name: 'brochure.docx', builder: buildBrochure },
    { name: 'cma-report.docx', builder: buildCma },
    { name: 'comparison.docx', builder: buildComparison },
  ];

  for (const { name, builder } of templates) {
    const doc = builder();
    const buffer = await Packer.toBuffer(doc);
    const filePath = path.join(TEMPLATES_DIR, name);
    fs.writeFileSync(filePath, buffer);
    console.log(`✅  ${name}  (${(buffer.length / 1024).toFixed(1)} KB)`);
  }

  console.log(`\n📁 Templates written to: ${TEMPLATES_DIR}`);
  console.log('   Ready for Carbone rendering with your AI-generated JSON data.');
}

main().catch((err) => {
  console.error('❌ Template generation failed:', err.message);
  process.exit(1);
});
